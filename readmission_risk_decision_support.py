from sklearn.linear_model import LogisticRegression
from sklearn.tree import DecisionTreeClassifier
from sklearn.ensemble import RandomForestClassifier, GradientBoostingClassifier

# ------------------------------------------------------------
# 1. Split the data into train and test sets
# ------------------------------------------------------------
X_train, X_test, y_train, y_test = train_test_split(
    X,
    y,
    test_size=0.20,
    random_state=42,
    stratify=y
)

print("Training Feature Shape:", X_train.shape)
print("Testing Feature Shape:", X_test.shape)
print("Training Target Shape:", y_train.shape)
print("Testing Target Shape:", y_test.shape)

print("\n" + "="*80 + "\n")

# ------------------------------------------------------------
# 2. Identify numerical and categorical columns
# ------------------------------------------------------------
numerical_cols = X_train.select_dtypes(include=["int64", "float64"]).columns.tolist()
categorical_cols = X_train.select_dtypes(include=["object"]).columns.tolist()

print("Numerical Columns:")
print(numerical_cols)

print("\nCategorical Columns:")
print(categorical_cols)

print("\n" + "="*80 + "\n")

# ------------------------------------------------------------
# 3. Create preprocessing transformer
# ------------------------------------------------------------
preprocessor = ColumnTransformer(
    transformers=[
        ("num", StandardScaler(), numerical_cols),
        ("cat", OneHotEncoder(handle_unknown="ignore"), categorical_cols)
    ]
)

# ------------------------------------------------------------
# 4. Define model pipelines
# ------------------------------------------------------------

# Logistic Regression Pipeline
log_reg_pipeline = Pipeline(steps=[
    ("preprocessor", preprocessor),
    ("classifier", LogisticRegression(max_iter=1000, class_weight="balanced", random_state=42))
])

# Decision Tree Pipeline
decision_tree_pipeline = Pipeline(steps=[
    ("preprocessor", preprocessor),
    ("classifier", DecisionTreeClassifier(class_weight="balanced", random_state=42))
])

# Random Forest Pipeline
random_forest_pipeline = Pipeline(steps=[
    ("preprocessor", preprocessor),
    ("classifier", RandomForestClassifier(
        n_estimators=100,
        class_weight="balanced",
        random_state=42,
        n_jobs=-1
    ))
])

# Gradient Boosting Pipeline
gradient_boosting_pipeline = Pipeline(steps=[
    ("preprocessor", preprocessor),
    ("classifier", GradientBoostingClassifier(random_state=42))
])

# ------------------------------------------------------------
# 5. Store all pipelines in a dictionary
# ------------------------------------------------------------
models = {
    "Logistic Regression": log_reg_pipeline,
    "Decision Tree": decision_tree_pipeline,
    "Random Forest": random_forest_pipeline,
    "Gradient Boosting": gradient_boosting_pipeline
}

# ------------------------------------------------------------
# 6. Confirm the model pipelines
# ------------------------------------------------------------
print("Model Pipelines Created Successfully:")
for model_name in models.keys():
    print("-", model_name)

# ============================================================
# STEP 9: TRAIN MODELS AND GENERATE PREDICTIONS
